// gen_public_style_corpus
//
// One-shot generator for the public-style evidence corpus in
// `untracked/commander-v1.4/short-message-corpus-sources/public-style/`.
//
// Emits deterministic plain-text files. Each line is one message.
// Lines starting with `#` are comments at the top of the file.

use std::fs;
use std::io::Write;
use std::path::PathBuf;

mod emit;
mod seed;
mod vocab;

fn out_dir() -> PathBuf {
    PathBuf::from("untracked/commander-v1.4/short-message-corpus-sources/public-style")
}

fn write_file(path: &PathBuf, header: &[&str], body: &str) -> std::io::Result<()> {
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent)?;
    }
    let mut f = fs::File::create(path)?;
    for h in header {
        writeln!(f, "# {}", h)?;
    }
    writeln!(f)?;
    f.write_all(body.as_bytes())?;
    Ok(())
}

fn main() {
    let dir = out_dir();
    fs::create_dir_all(&dir).expect("create public-style dir");

    let mut rng = seed::SeedRng::new(0xC0FFEE_BABE_2026);

    let pairs: &[(&str, &str, &str, &[&str], usize)] = &[
        (
            "crypto-forum.txt",
            "crypto_hemp0x",
            "Public-style crypto support / wallet / block explorer / mining chat. Synthetic, locally generated. Not copied from any real forum.",
            &[],
            1500,
        ),
        (
            "sales-fulfillment.txt",
            "business_payment",
            "Public-style invoices, orders, returns, wholesale, retail, support replies. Synthetic, locally generated.",
            &[],
            1500,
        ),
        (
            "everyday-chat.txt",
            "casual_chat",
            "Public-style family/friend coordination, plans, delays, emoji-heavy chat. Synthetic, locally generated.",
            &[],
            1500,
        ),
        (
            "ops-support.txt",
            "operations_support",
            "Public-style tickets, standups, deploys, incidents, status checks. Synthetic, locally generated.",
            &[],
            1500,
        ),
        (
            "logistics-provenance.txt",
            "logistics_trace",
            "Public-style shipping, harvest, batch, lab, COA, supplier, delivery, traceability. Synthetic, locally generated.",
            &[],
            1500,
        ),
        (
            "edge-cases.txt",
            "edge_cases",
            "CIDs, txids, addresses, prices, dates, short URLs, phone-ish and tracking-ish strings. Synthetic, locally generated.",
            &[],
            1500,
        ),
    ];

    let mut total = 0usize;
    for (filename, category, header, _, n) in pairs {
        let body = match *category {
            "crypto_hemp0x" => emit::crypto_forum(&mut rng, *n),
            "business_payment" => emit::sales_fulfillment(&mut rng, *n),
            "casual_chat" => emit::everyday_chat(&mut rng, *n),
            "operations_support" => emit::ops_support(&mut rng, *n),
            "logistics_trace" => emit::logistics_provenance(&mut rng, *n),
            "edge_cases" => emit::edge_cases(&mut rng, *n),
            _ => String::new(),
        };
        let lc = body.lines().count();
        let path = dir.join(filename);
        write_file(
            &path,
            &[header, "", "Generated by gen_public_style_corpus."],
            &body,
        )
        .expect("write file");
        total += lc;
        println!("wrote {} ({} samples)", path.display(), lc);
    }
    println!("total samples: {}", total);
}
