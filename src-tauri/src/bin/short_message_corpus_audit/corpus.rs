// Built-in short-message corpus used by the audit binary.
//
// The categories mirror the dictionary role split in
// `short_message_tables.rs`. Samples are short, realistic, and intentionally
// written so that they actually exercise the codec.

use super::Category;

pub(super) fn built_in_corpus() -> Vec<super::Sample> {
    let mut out: Vec<super::Sample> = Vec::with_capacity(900);
    out.extend(casual_chat());
    out.extend(business_payment());
    out.extend(operations_support());
    out.extend(asset_holder());
    out.extend(logistics_trace());
    out.extend(crypto_hemp0x());
    out.extend(edge_cases());
    out
}

fn cat(category: Category) -> impl Fn(&'static str) -> super::Sample {
    move |text| super::Sample {
        text: text.to_string(),
        category,
        source: "built-in".to_string(),
    }
}

